#!/usr/bin/env node
import { existsSync, mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import ts from 'typescript';

const apiRoot = join(process.cwd(), 'src');
const routesRoot = join(apiRoot, 'core/routes/api');
const output = join(apiRoot, 'core/permissions/generated/core-permissions.ts');
const check = process.argv.includes('--check');

function walk(dir) {
  return readdirSync(dir, { withFileTypes: true }).flatMap((entry) => {
    const file = join(dir, entry.name);
    if (entry.isDirectory()) return walk(file);
    return entry.name === 'permissions.ts' ? [file] : [];
  });
}

function validate(file) {
  const source = readFileSync(file, 'utf8');
  const ast = ts.createSourceFile(file, source, ts.ScriptTarget.Latest, true);
  const hasDefault = ast.statements.some((statement) =>
    ts.isExportAssignment(statement) && !statement.isExportEquals,
  );
  if (!hasDefault || !/definePermissions\s*\(/.test(source)) {
    throw new Error(`${relative(process.cwd(), file)} must default-export definePermissions({...})`);
  }
}

const files = walk(routesRoot).sort();
files.forEach(validate);
const imports = files.map((file, index) => {
  const specifier = relative(dirname(output), file).replace(/\\/g, '/').replace(/\.ts$/, '');
  return `import p${index} from '${specifier.startsWith('.') ? specifier : `./${specifier}`}.js';`;
});
const content = `// Generated by scripts/generate-core-permissions.mjs. Do not edit.\n${imports.join('\n')}\n\nimport type { PermissionRef } from '../define-permissions.js';\n\nconst sources = [${files.map((_, index) => `p${index}`).join(', ')}] as const;\nexport const corePermissionDefinitions: readonly PermissionRef[] = Object.freeze(sources.flatMap((source) => Object.values(source)));\n\nconst seen = new Set<string>();\nfor (const permission of corePermissionDefinitions) {\n  if (seen.has(permission.code)) throw new Error(\`duplicate Core permission declaration: \${permission.code}\`);\n  seen.add(permission.code);\n}\n`;

if (check) {
  if (!existsSync(output) || readFileSync(output, 'utf8') !== content) {
    console.error('Core permission registry is stale. Run: pnpm --filter yishan-api permissions:generate');
    process.exit(1);
  }
} else {
  mkdirSync(dirname(output), { recursive: true });
  writeFileSync(output, content);
}
